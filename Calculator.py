def addition(a, b):
    res = a + b
    return res

def subtraction(a, b):
    res = a - b
    return res  

def multiplication(a, b):
    res = a * b
    return res

def division(a, b):
    res = a / b
    return res

a = 10
b = 50

print('Addition:',addition(a, b))
print('Subtraction:',subtraction(a, b))
print('Multiplication:',multiplication(a, b))
print('Division:',division(a, b))
